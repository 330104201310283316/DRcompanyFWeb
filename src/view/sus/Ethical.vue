    methods:{
      checkScroll:function(){
          var _this = this;
          var visibleBottom = window.scrollY+document.documentElement.clientHeight;
          var visibleTop = window.scrollY;
          
          var list = ["image1",'image2'];
          for(var i=0;i<list.length;i++){
              var itemDom = document.getElementById(list[i]);
              var centerY = itemDom.offsetTop + (itemDom.offsetHeight*0.8);
              var res = centerY > visibleTop && centerY < visibleBottom;
              if(res){
                  var show = {
                      ...this.show,
                  };
                  show[list[i]] = res;
                  _this.show = show;
              }
          }
      }  
    },
    mounted:function(){
        var _this = this;
        setTimeout(function(){
            _this.show = true;
        },500);
        
